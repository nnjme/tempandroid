package com.changlianxi.modle;

public class GrowthImgModle {
	/**
	 * 成长记录中图片信息
	 */
	String id = "";// 图片id
	String img = "";// 图片地址
	String samllImg = "";// 图片小图地址

	public String getSamllImg() {
		return samllImg;
	}

	public void setSamllImg(String samllImg) {
		this.samllImg = samllImg;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

}
